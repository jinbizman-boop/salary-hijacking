import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

import {
  DEFAULT_SECRET_STORES,
  REQUIRED_RUNTIME_SECRET_NAMES,
} from "./generate-secrets-evidence.mjs";

const DEFAULT_OUTPUT_PATH = "release/secrets-proof.local.json";
const LOCAL_STORE = "local process environment";

const APPROVED_PROOF_STORES = new Set([
  LOCAL_STORE,
  "GitHub Environments",
  "GitHub Actions runtime",
  "Cloudflare Worker secret",
  "Cloudflare account secret",
  "Neon",
  "EAS secret store",
  "EAS project settings",
  "provider secret store",
  "release external evidence",
]);

const requiredSecretNameSet = new Set(REQUIRED_RUNTIME_SECRET_NAMES);

const resolveFromCwd = (filePath) =>
  path.isAbsolute(filePath) ? filePath : path.join(process.cwd(), filePath);

const normalizeProofStore = (proofStore) => {
  const candidate =
    typeof proofStore === "string" && proofStore.trim()
      ? proofStore.trim()
      : LOCAL_STORE;
  return APPROVED_PROOF_STORES.has(candidate) ? candidate : LOCAL_STORE;
};

const normalizeSecretNames = (secretNames) => {
  const candidateNames =
    typeof secretNames === "string" ? secretNames.split(",") : secretNames;
  const names = Array.isArray(candidateNames)
    ? candidateNames
        .filter((name) => typeof name === "string")
        .map((name) => name.trim())
        .filter(Boolean)
    : [...REQUIRED_RUNTIME_SECRET_NAMES];

  const unknown = names.filter((name) => !requiredSecretNameSet.has(name));
  if (unknown.length > 0) {
    throw new Error(
      `secret proof collector contains unknown secret names: ${unknown.join(", ")}`,
    );
  }

  return names;
};

const hasPresentEnvValue = (env, name) =>
  typeof env?.[name] === "string" && env[name].trim().length > 0;

const buildEntry = ({ env, name, proofStore }) => {
  const present = hasPresentEnvValue(env, name);
  const trustedStore = proofStore !== LOCAL_STORE;
  const verified = present && trustedStore;
  const stores = trustedStore ? [proofStore] : [LOCAL_STORE];
  const fallbackStores = DEFAULT_SECRET_STORES[name] ?? stores;

  return {
    verified,
    stores: stores.length > 0 ? stores : fallbackStores,
    note: verified
      ? `${name} presence observed in ${proofStore} without writing the value.`
      : `${name} presence is not verified for release; collect proof from an approved secret store without writing the value.`,
  };
};

export const buildSecretsProof = ({
  env = process.env,
  proofStore = env?.SECRET_PROOF_STORE ?? process.env.SECRET_PROOF_STORE,
  secretNames = env?.SECRET_PROOF_NAMES,
  now = () => new Date(),
} = {}) => {
  const normalizedStore = normalizeProofStore(proofStore);
  const names = normalizeSecretNames(secretNames);
  const secrets = Object.fromEntries(
    names.map((name) => [
      name,
      buildEntry({ env, name, proofStore: normalizedStore }),
    ]),
  );

  return {
    schemaVersion: 1,
    observedAt: now().toISOString(),
    source:
      "Generated by scripts/release/collect-secrets-proof.mjs from runtime environment key presence only; secret values are never written",
    secretsRedacted: true,
    containsSecretValues: false,
    secrets,
    note: "This local proof stores only secret-name presence booleans, approved store names, and notes. Do not add raw database URLs, tokens, DSNs, webhooks, private keys, service accounts, passwords, or copied provider payloads.",
  };
};

export const collectSecretsProof = ({
  env = process.env,
  proofStore = env?.SECRET_PROOF_STORE ?? process.env.SECRET_PROOF_STORE,
  secretNames = env?.SECRET_PROOF_NAMES,
  outputPath = DEFAULT_OUTPUT_PATH,
  now = () => new Date(),
  writeFile = true,
} = {}) => {
  const proof = buildSecretsProof({ env, proofStore, secretNames, now });

  if (writeFile) {
    const absoluteOutputPath = resolveFromCwd(outputPath);
    fs.mkdirSync(path.dirname(absoluteOutputPath), { recursive: true });
    fs.writeFileSync(
      absoluteOutputPath,
      `${JSON.stringify(proof, null, 2)}\n`,
      "utf8",
    );
  }

  return proof;
};

const isCliEntrypoint = () =>
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isCliEntrypoint()) {
  const proof = collectSecretsProof();
  const verified = Object.values(proof.secrets).every(
    (entry) => entry.verified === true,
  );
  console.log(
    `[secrets-proof] wrote ${DEFAULT_OUTPUT_PATH}; verified=${verified}`,
  );
  if (!verified) process.exitCode = 1;
}
