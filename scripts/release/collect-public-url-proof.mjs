import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const DEFAULT_BASE_URL = "https://salaryhijacking.com";
const DEFAULT_OUTPUT_PATH = "release/public-url-proof.local.json";

const PUBLIC_PAGE_SPECS = Object.freeze([
  {
    key: "landing",
    path: "/",
    reachabilityKey: "landingReachable",
    requiredText: ["급여납치", "이번 달 내가 지켜낸 돈"],
  },
  {
    key: "privacy",
    path: "/privacy",
    reachabilityKey: "privacyReachable",
    requiredText: ["급여납치", "개인정보"],
  },
  {
    key: "support",
    path: "/support",
    reachabilityKey: "supportReachable",
    requiredText: ["급여납치", "지원"],
  },
  {
    key: "terms",
    path: "/terms",
    reachabilityKey: "termsReachable",
    requiredText: ["급여납치", "이용약관"],
  },
]);

const SENSITIVE_PUBLIC_PAGE_PATTERN =
  /(salaryAmount|expenseAmount|savingAmount|savingsAmount|hijackAmount|accountNumber|cardNumber|loan|resident|phoneNumber|authToken|refreshToken|sessionToken|pushToken|rawDeviceIdentifier|DATABASE_URL|JWT_SECRET|PRIVATE_KEY|SERVICE_ACCOUNT|FCM_SERVER_KEY)/i;

const SENSITIVE_PUBLIC_HEADER_PATTERN =
  /(set-cookie|authorization|cookie|email|phone|token|session|auth|device|account|card|loan|database_url|jwt_secret|private_key|service_account|fcm_server_key|salary|expense|saving|hijack)/i;

const normalizeBaseUrl = (baseUrl) => {
  const value = String(baseUrl ?? DEFAULT_BASE_URL).trim() || DEFAULT_BASE_URL;
  return value.replace(/\/+$/, "");
};

const pageUrl = (baseUrl, pagePath) => {
  const normalized = normalizeBaseUrl(baseUrl);
  return `${normalized}${pagePath === "/" ? "/" : pagePath}`;
};

const headerValue = (headers, name) => {
  const value = headers.get(name);
  return typeof value === "string" ? value : "";
};

const hasHtmlStatus = (response) =>
  response.status >= 200 &&
  response.status < 300 &&
  headerValue(response.headers, "content-type")
    .toLowerCase()
    .includes("text/html");

const hasCsp = (response) =>
  headerValue(response.headers, "content-security-policy")
    .toLowerCase()
    .includes("default-src");

const hasPrivacyHeaders = (response) => {
  const financialRaw = headerValue(
    response.headers,
    "x-financial-raw-data-exposed",
  ).toLowerCase();
  const adTargeting = headerValue(
    response.headers,
    "x-ad-financial-targeting",
  ).toLowerCase();
  return financialRaw === "false" && adTargeting === "separated";
};

const noIndexAbsent = (response, body) => {
  const robotHeader = headerValue(response.headers, "x-robots-tag");
  return !/noindex/i.test(robotHeader) && !/noindex/i.test(body);
};

const hasRequiredKoreanCopy = (body, requiredText) =>
  requiredText.every((text) => body.includes(text));

const hasStoreReviewLinks = (body) =>
  body.includes("/privacy") &&
  body.includes("/support") &&
  body.includes("/terms");

const hasSensitiveHeader = (headers) => {
  for (const [name, value] of headers.entries()) {
    const header = `${name}:${value}`;
    if (
      SENSITIVE_PUBLIC_HEADER_PATTERN.test(header) ||
      SENSITIVE_PUBLIC_PAGE_PATTERN.test(header)
    ) {
      return true;
    }
  }
  return false;
};

const emptyPageResult = (spec, error) => ({
  spec,
  reachable: false,
  csp: false,
  privacyHeaders: false,
  noIndexAbsent: false,
  koreanCopy: false,
  sensitiveSafe: false,
  storeReviewLinks: false,
  error,
});

const fetchPage = async ({ spec, baseUrl, fetchImpl }) => {
  try {
    const response = await fetchImpl(pageUrl(baseUrl, spec.path), {
      method: "GET",
      headers: { accept: "text/html" },
    });
    const body = await response.text();
    return {
      spec,
      reachable: hasHtmlStatus(response),
      csp: hasCsp(response),
      privacyHeaders: hasPrivacyHeaders(response),
      noIndexAbsent: noIndexAbsent(response, body),
      koreanCopy: hasRequiredKoreanCopy(body, spec.requiredText),
      sensitiveSafe:
        !SENSITIVE_PUBLIC_PAGE_PATTERN.test(body) &&
        !hasSensitiveHeader(response.headers),
      storeReviewLinks: spec.path === "/" ? hasStoreReviewLinks(body) : true,
      error: "",
    };
  } catch (error) {
    return emptyPageResult(
      spec,
      error instanceof Error ? error.message : "fetch failed",
    );
  }
};

const allPages = (results, selector) =>
  results.length === PUBLIC_PAGE_SPECS.length && results.every(selector);

export const buildPublicUrlProof = ({ results, now = () => new Date() }) => {
  const reachability = Object.fromEntries(
    results.map((result) => [result.spec.reachabilityKey, result.reachable]),
  );

  return {
    schemaVersion: 1,
    observedAt: now().toISOString(),
    source:
      "Generated by scripts/release/collect-public-url-proof.mjs from live public URL smoke checks; raw response bodies, headers, logs, and user/financial payloads are not written",
    secretsRedacted: true,
    containsSecretValues: false,
    reachability,
    headers: {
      cspVerified: allPages(results, (result) => result.csp),
      privacyHeadersVerified: allPages(
        results,
        (result) => result.privacyHeaders,
      ),
      noIndexAbsentOnPublicPages: allPages(
        results,
        (result) => result.noIndexAbsent,
      ),
    },
    content: {
      koreanCopyVerified: allPages(results, (result) => result.koreanCopy),
      storeReviewUrlsVerified:
        Boolean(reachability.landingReachable) &&
        Boolean(reachability.privacyReachable) &&
        Boolean(reachability.supportReachable) &&
        Boolean(reachability.termsReachable) &&
        results.find((result) => result.spec.path === "/")?.storeReviewLinks ===
          true,
      noSensitiveRawDataExposed: allPages(
        results,
        (result) => result.sensitiveSafe,
      ),
    },
    note: "This local proof intentionally stores only booleans. Do not add raw HTML, copied headers, response bodies, logs, emails, salary, expense, savings, hijack amounts, tokens, or database URLs.",
  };
};

export const collectPublicUrlProof = async ({
  baseUrl = process.env.PUBLIC_APP_BASE_URL ?? DEFAULT_BASE_URL,
  fetchImpl = globalThis.fetch,
  outputPath = DEFAULT_OUTPUT_PATH,
  now = () => new Date(),
  writeFile = true,
} = {}) => {
  if (typeof fetchImpl !== "function") {
    throw new TypeError("fetchImpl must be a function");
  }

  const results = await Promise.all(
    PUBLIC_PAGE_SPECS.map((spec) => fetchPage({ spec, baseUrl, fetchImpl })),
  );
  const proof = buildPublicUrlProof({ results, now });

  if (writeFile) {
    const absoluteOutputPath = path.isAbsolute(outputPath)
      ? outputPath
      : path.join(process.cwd(), outputPath);
    fs.mkdirSync(path.dirname(absoluteOutputPath), { recursive: true });
    fs.writeFileSync(
      absoluteOutputPath,
      `${JSON.stringify(proof, null, 2)}\n`,
      "utf8",
    );
  }

  return proof;
};

const isCliEntrypoint = () =>
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isCliEntrypoint()) {
  const proof = await collectPublicUrlProof();
  const verified =
    Object.values(proof.reachability).every(Boolean) &&
    Object.values(proof.headers).every(Boolean) &&
    Object.values(proof.content).every(Boolean);
  console.log(
    `[public-url-proof] wrote ${DEFAULT_OUTPUT_PATH}; verified=${verified}`,
  );
  if (!verified) process.exitCode = 1;
}
